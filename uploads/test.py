# list_utils.py
sample_list = [1, 2, 3, 4, 5]

def sum_list(lst):
    """Return sum of all elements in the list."""
    return sum(lst)

def max_in_list(lst):
    """Return the maximum element in the list."""
    return max(lst)

def min_in_list(lst):
    """Return the minimum element in the list."""
    return min(lst)

def reverse_list(lst):
    """Return the reversed list."""
    return lst[::-1]
